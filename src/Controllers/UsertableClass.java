package Controllers;

import java.util.Date;

public class UsertableClass {
    int state,id;
    String fname,lname,gender,address,phone,username,password,email;
    String bdate;

    public UsertableClass( int id, String fname, String lname, String gender, String address, String bdate, String phone, String username, String password, String email,int state) {
        setState(state);
        setId(id);
        setFname(fname);
        setLname(lname);
        setGender(gender);
        setAddress(address);
        setBdate(bdate);
        setPhone(phone);
        setUsername(username);
        setPassword(password);
        setEmail(email);
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBdate() {
        return bdate;
    }

    public void setBdate(String bdate) {
        this.bdate = bdate;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
